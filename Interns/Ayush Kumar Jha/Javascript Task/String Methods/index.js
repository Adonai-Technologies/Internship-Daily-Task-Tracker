// Example string
let str = "Ayush, Jha";

// Method: toUpperCase()
console.log("toUpperCase():", str.toUpperCase());

// Method: toLowerCase()
console.log("toLowerCase():", str.toLowerCase());

// Method: concat()
console.log("concat():", str.concat(" I'm here!"));

// Method: indexOf()
console.log("indexOf('Jha'):", str.indexOf("Jha"));

// Method: slice()
console.log("slice(1, 4):", str.slice(1, 4));

// Method: substring()
console.log("substring(1, 3):", str.substring(1, 3));

// Method: replace()
console.log("replace('Jha', 'Kumar'):", str.replace("Jha", "Kumar"));

// Method: split()
console.log("split(','): ", str.split(","));

// Method: charAt()
console.log("charAt(7):", str.charAt(7));

// Method: trim()
let whitespaceStr = "   Trim me!   ";
console.log("trim():", whitespaceStr.trim());

